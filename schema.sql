-- Database creation
CREATE DATABASE IF NOT EXISTS pr_mis_db;
USE pr_mis_db;

CREATE TABLE IF NOT EXISTS metas (
  id INT AUTO_INCREMENT PRIMARY KEY,
  grupo_portafolio VARCHAR(100) NOT NULL,
  cliente VARCHAR(100) NOT NULL,
  monto_meta DECIMAL(15,2) NOT NULL,
  mes VARCHAR(7) NOT NULL
);

CREATE TABLE IF NOT EXISTS acumulado_pagos (
  id INT AUTO_INCREMENT PRIMARY KEY,
  cliente VARCHAR(100) NOT NULL,
  monto_pago DECIMAL(15,2) NOT NULL,
  fecha_pago DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS aranceles (
  id INT AUTO_INCREMENT PRIMARY KEY,
  grupo_portafolio VARCHAR(100) NOT NULL,
  nombre_arancel VARCHAR(50) NOT NULL,
  porcentaje_minimo DECIMAL(5,2) NOT NULL,
  tasa_comision DECIMAL(5,2) NOT NULL
);

